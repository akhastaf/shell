#include "../../includes/minishell.h"

void	ft_quotes(char c, int *q, int *sq)
{
	if (c == '"' && !(*q) && !(*sq))
		*q = 1;
	else if (c == '"' && *q)
		*q = 0;
	if (c == '\'' && !(*sq) && !(*q))
		*sq = 1;
	else if (c == '\'' && *sq)
		*sq = 0;
}
