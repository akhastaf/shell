#include "../../includes/minishell.h"

int	ft_isvalidarg(char *s)
{
	int	i;

	i = 0;
	while (s[i])
	{
		if (!((s[i] >= 48 && s[i] <= 57) || (s[i] >= 65 && s[i] <= 90)
				|| (s[i] >= 97 && s[i] <= 122) || s[i] == '_' || s[i] == '+'))
			return (0);
		i++;
	}
	return (1);
}
